        <li>สำเนาบัตรประชาชนของผู้สั่งซื้อ พร้อมเซ็นมอบอำนาจ รับรองเอกสารถูกต้อง</li>
        <li>เอกสารยืนยันคำสั่งซื้อสินค้า (Order Confirmation) ที่ได้รับผ่านทาง Email Address โดยลูกค้าสามารถแสดงข้อมูลหลักฐานผ่านอุปกรณ์ โทรศัพท์มือถือ หรือแท็บเล็ต เพื่อแสดงต่อเจ้าหน้าที่ได้</li>
